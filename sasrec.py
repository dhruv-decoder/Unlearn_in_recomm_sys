def build_sequences(df, max_len=MAX_SEQ_LEN):
    """Build per-user item sequences sorted by time."""
    seqs = defaultdict(list)
    for row in df.itertuples():
        seqs[row.user].append(row.item)
    # keep only users with >= 3 interactions
    seqs = {u: items for u, items in seqs.items() if len(items) >= 3}
    return seqs

def train_test_split_seqs(seqs):
    """Leave-one-out: last item = test, second-last = val, rest = train."""
    train_seqs, val_seqs, test_seqs = {}, {}, {}
    for u, items in seqs.items():
        train_seqs[u] = items[:-2]
        val_seqs[u]   = items[:-1]   # up to second-last for input, last-1 as target
        test_seqs[u]  = items         # full sequence, last item as target
    return train_seqs, val_seqs, test_seqs


