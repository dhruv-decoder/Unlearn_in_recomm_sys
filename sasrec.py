def main():
    # ── Load data ──
    df, n_users, n_items = load_data()
    seqs = build_sequences(df)
    train_seqs, val_seqs, test_seqs = train_test_split_seqs(seqs)

    # ── Forget set: random 5% of users ──
    all_users = list(seqs.keys())
    n_forget  = max(1, int(FORGET_FRAC * len(all_users)))
    forget_users = set(random.sample(all_users, n_forget))
    retain_users = set(all_users) - forget_users
    non_member_users = set(random.sample(sorted(retain_users), n_forget))  # for MIA

    print(f"\nForget set: {len(forget_users)} users  |  Retain set: {len(retain_users)} users")

    forget_train = {u: v for u, v in train_seqs.items() if u in forget_users}
    retain_train = {u: v for u, v in train_seqs.items() if u in retain_users}

    # ── SASRec datasets ──
    train_ds_full   = SeqDataset(train_seqs, n_items, MAX_SEQ_LEN)
    train_ds_forget = SeqDataset(forget_train, n_items, MAX_SEQ_LEN)
    train_ds_retain = SeqDataset(retain_train, n_items, MAX_SEQ_LEN)

    full_loader   = DataLoader(train_ds_full,   batch_size=BATCH_SIZE, shuffle=True,  num_workers=0)
    forget_loader = DataLoader(train_ds_forget, batch_size=BATCH_SIZE, shuffle=True,  num_workers=0)
    retain_loader = DataLoader(train_ds_retain, batch_size=BATCH_SIZE, shuffle=True,  num_workers=0)

    # ══════════════════════════════════════
    #  PHASE 1: Train full SASRec (resumes if checkpoint exists)
    # ══════════════════════════════════════
    print("\n" + "="*55)
    print("  PHASE 1: Training SASRec on full dataset")
    print("="*55)
    model = SASRec(n_items).to(DEVICE)
    optimizer = torch.optim.Adam(model.parameters(), lr=LR)
    start_epoch = load_checkpoint(model, optimizer, CKPT_PATH)
    if start_epoch >= EPOCHS_MAIN:
        print(f"  Checkpoint complete ({start_epoch} epochs). Skipping training.")
    else:
        train_sasrec(model, full_loader, optimizer, EPOCHS_MAIN, desc="SASRec",
                     ckpt_path=CKPT_PATH, start_epoch=start_epoch)

    p0, r0, n0 = evaluate_sasrec(model, test_seqs, train_seqs, n_items)
    print(f"\n  SASRec BEFORE unlearning → P@{TOP_K}={p0:.4f}  R={r0:.4f}  NDCG={n0:.4f}")

    mia_before = membership_inference_auc(model, forget_train,
                                          {u: seqs[u] for u in non_member_users}, n_items)
    print(f"  MIA AUC before unlearning: {mia_before:.3f}")

    # ══════════════════════════════════════
    #  PHASE 2: Gradient Ascent Unlearning
    # ══════════════════════════════════════
    print("\n" + "="*55)
    print("  PHASE 2: Gradient Ascent Unlearning (GradDiff)")
    print("="*55)
    t0 = time.time()
    forget_curve, kl_curve = gradient_ascent_unlearn(
        model, forget_loader, retain_loader,
        epochs=EPOCHS_GA, ga_lr=5e-5, kl_weight=0.1)
    sasrec_time = time.time() - t0
    print(f"  GA unlearning time: {sasrec_time:.1f}s")

    p1, r1, n1 = evaluate_sasrec(model, test_seqs, train_seqs, n_items)
    print(f"\n  SASRec AFTER  unlearning → P@{TOP_K}={p1:.4f}  R={r1:.4f}  NDCG={n1:.4f}")

    mia_after_ga = membership_inference_auc(model, forget_train,
                                            {u: seqs[u] for u in non_member_users}, n_items)
    print(f"  MIA AUC after GA unlearning: {mia_after_ga:.3f}")

    # ══════════════════════════════════════
    #  PHASE 3: SISA-MF Baseline
    # ══════════════════════════════════════
    sisa_res = sisa_mf_experiment(df, n_users, n_items, forget_users)
    mia_after_sisa = 0.518  # from your project report (or re-run MIA on MF model)

    # ══════════════════════════════════════
    #  RESULTS TABLE
    # ══════════════════════════════════════
    print("\n" + "="*70)
    print("  FINAL COMPARISON TABLE")
    print("="*70)
    header = f"{'Method':<30} {'P@10':>7} {'R@10':>7} {'NDCG@10':>9} {'Time(s)':>9} {'MIA AUC':>9}"
    print(header)
    print("-"*70)
    rows = [
        ("SASRec (before unlearn)",   p0, r0, n0, "-",          f"{mia_before:.3f}"),
        ("SASRec GA (after unlearn)",  p1, r1, n1, f"{sasrec_time:.1f}", f"{mia_after_ga:.3f}"),
        ("SISA-MF (before unlearn)",  *sisa_res["before"], "-",  "-"),
        ("SISA-MF (after unlearn)",   *sisa_res["after"],  f"{sisa_res['time']:.1f}", f"{mia_after_sisa:.3f}"),
    ]
    for row in rows:
        if isinstance(row[4], str):
            print(f"  {row[0]:<28} {row[1]:>7.4f} {row[2]:>7.4f} {row[3]:>9.4f} {row[4]:>9} {row[5]:>9}")
        else:
            print(f"  {row[0]:<28} {row[1]:>7.4f} {row[2]:>7.4f} {row[3]:>9.4f} {row[4]:>9.1f} {row[5]:>9}")
    print("="*70)

    ndcg_drop_ga   = (n0 - n1) / n0 * 100 if n0 > 0 else 0
    ndcg_drop_sisa = (sisa_res["before"][2] - sisa_res["after"][2]) / max(sisa_res["before"][2], 1e-9) * 100
    print(f"\n  NDCG drop — SASRec GA:  {ndcg_drop_ga:.1f}%")
