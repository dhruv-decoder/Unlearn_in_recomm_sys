class MatrixFactorization(nn.Module):
    def __init__(self, n_users, n_items, dim=MF_DIM):
        super().__init__()
        self.user_emb = nn.Embedding(n_users, dim)
        self.item_emb = nn.Embedding(n_items, dim)
        nn.init.normal_(self.user_emb.weight, std=0.01)
        nn.init.normal_(self.item_emb.weight, std=0.01)

    def forward(self, users, items):
        u = self.user_emb(users)
        i = self.item_emb(items)
        return (u * i).sum(-1)

class InteractionDataset(Dataset):
    def __init__(self, interactions, n_items, n_neg=4):
        self.pos = interactions
        self.n_items = n_items
        self.n_neg = n_neg
        item_set = set(interactions[:, 1].tolist())
        self.all_items = list(set(range(n_items)) - item_set)

    def __len__(self): return len(self.pos)

    def __getitem__(self, idx):
        u, i, label = self.pos[idx]
        negs = random.sample(self.all_items, self.n_neg)
        users  = torch.tensor([u] * (1 + self.n_neg), dtype=torch.long)
        items  = torch.tensor([i] + negs, dtype=torch.long)
        labels = torch.tensor([1.0] + [0.0] * self.n_neg)
        return users, items, labels

def train_mf(model, interactions, n_items, epochs=MF_EPOCHS, lr=MF_LR):
    ds = InteractionDataset(interactions, n_items)
    loader = DataLoader(ds, batch_size=BATCH_SIZE, shuffle=True)
    opt = torch.optim.Adam(model.parameters(), lr=lr, weight_decay=1e-5)
    criterion = nn.BCEWithLogitsLoss()
    model.train()
    for _ in range(epochs):
        for users, items, labels in loader:
            users, items, labels = users.to(DEVICE), items.to(DEVICE), labels.to(DEVICE)
            scores = model(users.view(-1), items.view(-1))
            loss = criterion(scores, labels.view(-1))
            opt.zero_grad(); loss.backward(); opt.step()

@torch.no_grad()
def evaluate_mf(model, test_interactions, train_set, n_items, k=TOP_K):
    model.eval()
    user_test = defaultdict(list)
    user_train = defaultdict(set)
    for u, i, _ in test_interactions:
        user_test[int(u)].append(int(i))
    for u, i, _ in train_set:
        user_train[int(u)].add(int(i))

    precisions, recalls, ndcgs = [], [], []
    all_items = torch.arange(n_items, device=DEVICE)

    for u, pos_items in list(user_test.items())[:500]:
        u_t  = torch.tensor([u], device=DEVICE)
        scores = model(u_t.expand(n_items), all_items)
        for train_i in user_train[u]:
            if train_i < n_items:
                scores[train_i] = -1e9
        top_k = scores.argsort(descending=True)[:k].cpu().tolist()
        hits  = [1 if i in pos_items else 0 for i in top_k]
        precisions.append(sum(hits) / k)
        recalls.append(sum(hits) / max(len(pos_items), 1))
        dcg  = sum(h / math.log2(r + 2) for r, h in enumerate(hits))
        idcg = sum(1 / math.log2(r + 2) for r in range(min(len(pos_items), k)))
        ndcgs.append(dcg / idcg if idcg > 0 else 0)

    return np.mean(precisions), np.mean(recalls), np.mean(ndcgs)

def sisa_mf_experiment(df, n_users, n_items, forget_users):
    """Run SISA-MF with K-means sharding (mirrors your existing project)."""
    print("\n" + "="*55)
    print("  SISA MATRIX FACTORIZATION BASELINE")
    print("="*55)

    interactions = df[["user", "item"]].copy()
    interactions["label"] = 1.0
    data = interactions.values.astype(np.int64)
    np.random.shuffle(data)
    split = int(0.8 * len(data))
    train_data, test_data = data[:split], data[split:]

    # K-means sharding
    print("  Building interaction matrix for K-means sharding …")
    from scipy.sparse import csr_matrix
    rows = train_data[:, 0]; cols = train_data[:, 1]
    mat  = csr_matrix((np.ones(len(rows)), (rows, cols)),
                      shape=(n_users, n_items))
    svd = TruncatedSVD(n_components=min(50, n_items - 1), random_state=SEED)
    user_vecs = svd.fit_transform(mat)
    km = KMeans(n_clusters=N_SHARDS, random_state=SEED, n_init=10)
    shard_labels = km.fit_predict(user_vecs)

    # Train shard models + checkpoints
    shard_models, shard_ckpts = [], []
    for s in range(N_SHARDS):
        shard_users  = np.where(shard_labels == s)[0]
        mask         = np.isin(train_data[:, 0], shard_users)
        shard_data   = train_data[mask]
        mf = MatrixFactorization(n_users, n_items).to(DEVICE)
        # slice checkpointing
        half = len(shard_data) // 2
        train_mf(mf, shard_data[:half], n_items, epochs=MF_EPOCHS // 2)
        ckpt = {k: v.clone() for k, v in mf.state_dict().items()}
        train_mf(mf, shard_data, n_items, epochs=MF_EPOCHS // 2)
        shard_models.append(mf)
        shard_ckpts.append(ckpt)

    def ensemble_predict(users_t, items_t):
        scores = torch.stack([m(users_t, items_t) for m in shard_models]).mean(0)
        return scores

    # Baseline metrics
    p0, r0, n0 = evaluate_mf(shard_models[0], test_data,
                              train_data.tolist(), n_items)
    print(f"  SISA-MF before unlearning → P@{TOP_K}={p0:.4f}  R={r0:.4f}  NDCG={n0:.4f}")

    # Unlearning
    forget_shard = shard_labels[list(forget_users)[0]] if len(forget_users) > 0 else 0
    t0 = time.time()
    mf_new = MatrixFactorization(n_users, n_items).to(DEVICE)
    mf_new.load_state_dict(shard_ckpts[forget_shard])
    retain_mask = ~np.isin(train_data[:, 0], list(forget_users))
    shard_mask  = np.isin(train_data[:, 0],
                          np.where(shard_labels == forget_shard)[0])
    retrain_data = train_data[retain_mask & shard_mask]
    if len(retrain_data) > 0:
        train_mf(mf_new, retrain_data, n_items, epochs=MF_EPOCHS)
    shard_models[forget_shard] = mf_new
    sisa_time = time.time() - t0

    p1, r1, n1 = evaluate_mf(shard_models[0], test_data,
                              train_data.tolist(), n_items)
    print(f"  SISA-MF after  unlearning → P@{TOP_K}={p1:.4f}  R={r1:.4f}  NDCG={n1:.4f}")
    print(f"  SISA unlearn time: {sisa_time:.1f}s")

    return dict(before=(p0, r0, n0), after=(p1, r1, n1), time=sisa_time)


