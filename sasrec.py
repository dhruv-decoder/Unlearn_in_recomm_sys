class SeqDataset(Dataset):
    """Sliding-window dataset for SASRec training."""
    def __init__(self, seqs, n_items, max_len, num_neg=1):
        self.samples = []
        self.n_items = n_items
        all_items = set(range(n_items))
        for u, items in seqs.items():
            if len(items) < 2:
                continue
            for i in range(1, len(items)):
                seq = items[max(0, i - max_len): i]
                pos = items[i] if i < len(items) else items[-1]
                neg_pool = list(all_items - set(items))
                negs = random.sample(neg_pool, num_neg)
                self.samples.append((u, seq, pos, negs))

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        u, seq, pos, negs = self.samples[idx]
        seq_padded = [0] * (MAX_SEQ_LEN - len(seq)) + seq
        seq_padded = seq_padded[-MAX_SEQ_LEN:]
        return (torch.tensor(u, dtype=torch.long),
                torch.tensor(seq_padded, dtype=torch.long),
                torch.tensor(pos, dtype=torch.long),
                torch.tensor(negs, dtype=torch.long))


