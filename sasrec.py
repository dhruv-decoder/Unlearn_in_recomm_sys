def bpr_loss(pos_score, neg_score):
    """BPR loss: maximize pos - neg."""
    return -F.logsigmoid(pos_score.unsqueeze(1) - neg_score).mean()

def train_sasrec(model, loader, optimizer, epochs, desc="Training",
                 ckpt_path=None, start_epoch=0):
    """Train SASRec with checkpoint save after every epoch for interrupt-safety."""
    model.train()
    for epoch in range(start_epoch, epochs):
        total_loss = 0
        for u, seq, pos, neg in tqdm(loader, desc=f"{desc} epoch {epoch+1}/{epochs}",
                                     leave=False):
            seq, pos, neg = seq.to(DEVICE), pos.to(DEVICE), neg.to(DEVICE)
            pos_s, neg_s = model(seq, pos, neg)
            loss = bpr_loss(pos_s, neg_s)
            optimizer.zero_grad(); loss.backward(); optimizer.step()
            total_loss += loss.item()
        avg_loss = total_loss / len(loader)
        print(f"  [{desc}] Epoch {epoch+1}/{epochs}: loss={avg_loss:.4f}")
        if ckpt_path:
            torch.save({"epoch": epoch + 1,
                        "model_state": model.state_dict(),
                        "optimizer_state": optimizer.state_dict(),
                        "loss": avg_loss}, ckpt_path)

def load_checkpoint(model, optimizer, ckpt_path):
    """Load checkpoint if it exists. Returns the epoch to resume from."""
    if os.path.exists(ckpt_path):
        ckpt = torch.load(ckpt_path, map_location=DEVICE)
        model.load_state_dict(ckpt["model_state"])
        optimizer.load_state_dict(ckpt["optimizer_state"])
        epoch = ckpt["epoch"]
        print(f"  Resumed from checkpoint at epoch {epoch} (loss={ckpt['loss']:.4f})")
        return epoch
    return 0

@torch.no_grad()
def evaluate_sasrec(model, test_seqs, train_seqs, n_items, k=TOP_K, max_users=500):
    """Compute Precision@k, Recall@k, NDCG@k via leave-one-out."""
    model.eval()
    precisions, recalls, ndcgs = [], [], []
    users = list(test_seqs.keys())[:max_users]

    for u in users:
        items = test_seqs[u]
        if len(items) < 2:
            continue
        ground_truth = {items[-1]}
        history      = set(train_seqs.get(u, []))

        seq = items[:-1]
        seq_t = [0] * (MAX_SEQ_LEN - len(seq)) + seq
        seq_t = seq_t[-MAX_SEQ_LEN:]
        seq_tensor = torch.tensor([seq_t], dtype=torch.long, device=DEVICE)

        scores = model.predict_all(seq_tensor)[0]  # (n_items+1,)
        scores[0] = -1e9  # mask pad
        for i in history:
            if i < scores.shape[0]:
                scores[i] = -1e9

        top_items = scores.argsort(descending=True)[:k].cpu().tolist()
        hits = [1 if i in ground_truth else 0 for i in top_items]

        precisions.append(sum(hits) / k)
        recalls.append(sum(hits) / len(ground_truth))
        dcg  = sum(h / math.log2(r + 2) for r, h in enumerate(hits))
        idcg = sum(1 / math.log2(r + 2) for r in range(min(len(ground_truth), k)))
        ndcgs.append(dcg / idcg if idcg > 0 else 0)

    return np.mean(precisions), np.mean(recalls), np.mean(ndcgs)


