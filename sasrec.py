@torch.no_grad()
def membership_inference_auc(model, forget_seqs, non_member_seqs, n_items, n_samples=200):
    """
    Train a simple logistic classifier on model confidence scores.
    Members (forget set) vs non-members → AUC close to 0.5 = good unlearning.
    """
    model.eval()
    feats, labels = [], []

    def get_score(seq_list):
        """Returns (target_score, max_softmax_prob) or None if scores are invalid."""
        seq   = seq_list[:-1]
        truth = seq_list[-1]
        if truth >= n_items:          # item index out of range
            return None
        seq_t = [0] * (MAX_SEQ_LEN - len(seq)) + seq
        seq_t = seq_t[-MAX_SEQ_LEN:]
        seq_tensor = torch.tensor([seq_t], dtype=torch.long, device=DEVICE)
        scores = model.predict_all(seq_tensor)[0]          # (n_items+1,)
        # Clamp to avoid ±inf after gradient ascent
        scores = torch.clamp(scores, min=-50.0, max=50.0)
        conf   = scores[truth].item()
        max_p  = F.softmax(scores, dim=0).max().item()
        if not (math.isfinite(conf) and math.isfinite(max_p)):
            return None
        return conf, max_p

    for u, items in list(forget_seqs.items())[:n_samples]:
        if len(items) < 2:
            continue
        result = get_score(items)
        if result is None:
            continue
        feats.append(list(result)); labels.append(1)

    for u, items in list(non_member_seqs.items())[:n_samples]:
        if len(items) < 2:
            continue
        result = get_score(items)
        if result is None:
            continue
        feats.append(list(result)); labels.append(0)

    if len(set(labels)) < 2 or len(feats) < 4:
        print("  [MIA] Not enough valid samples — returning 0.5")
        return 0.5

    feats_arr = np.array(feats, dtype=np.float32)
    # Final NaN/inf guard before sklearn
    mask = np.isfinite(feats_arr).all(axis=1)
    feats_arr = feats_arr[mask]
    labels_arr = np.array(labels)[mask]
    if len(set(labels_arr)) < 2:
        return 0.5

    clf = LogisticRegression(max_iter=500)
    clf.fit(feats_arr, labels_arr)
    proba = clf.predict_proba(feats_arr)[:, 1]
    return roc_auc_score(labels_arr, proba)


