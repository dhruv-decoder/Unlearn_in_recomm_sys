    print(f"  MIA improvement — SISA-MF:    {mia_before - mia_after_sisa:.3f} AUC reduction (reported)")

    # ══════════════════════════════════════
    #  PLOTS
    # ══════════════════════════════════════
    results_dict = dict(
        sasrec_before=(p0, r0, n0),
        sasrec_after=(p1, r1, n1),
        sisa_before=sisa_res["before"],
        sisa_after=sisa_res["after"],
        sasrec_time=sasrec_time,
        sisa_time=sisa_res["time"],
    )
    ga_history = dict(forget=forget_curve, kl=kl_curve)
    plot_results(results_dict, ga_history)
    plot_mia(mia_before, mia_after_ga, mia_after_sisa)

    print("\n  Done! Files generated:")
    print("    unlearning_comparison.png")
    print("    mia_comparison.png")


