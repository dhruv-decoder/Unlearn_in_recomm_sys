class PointWiseFFN(nn.Module):
    def __init__(self, d, dropout):
        super().__init__()
        self.net = nn.Sequential(
            nn.Linear(d, d * 4), nn.GELU(), nn.Dropout(dropout),
            nn.Linear(d * 4, d), nn.Dropout(dropout))
        self.norm = nn.LayerNorm(d)
    def forward(self, x):
        return self.norm(x + self.net(x))

class SASRecBlock(nn.Module):
    def __init__(self, d, heads, dropout):
        super().__init__()
        self.attn = nn.MultiheadAttention(d, heads, dropout=dropout, batch_first=True)
        self.norm = nn.LayerNorm(d)
        self.ffn  = PointWiseFFN(d, dropout)
        self.drop = nn.Dropout(dropout)

    def forward(self, x, attn_mask=None, key_padding_mask=None):
        residual = x
        x2, _ = self.attn(x, x, x, attn_mask=attn_mask,
                           key_padding_mask=key_padding_mask)
        x = self.norm(residual + self.drop(x2))
        return self.ffn(x)

class SASRec(nn.Module):
    def __init__(self, n_items, d=EMBED_DIM, heads=NUM_HEADS,
                 layers=NUM_LAYERS, max_len=MAX_SEQ_LEN, dropout=DROPOUT):
        super().__init__()
        self.item_emb = nn.Embedding(n_items + 1, d, padding_idx=0)
        self.pos_emb  = nn.Embedding(max_len, d)
        self.emb_drop = nn.Dropout(dropout)
        self.blocks   = nn.ModuleList(
            [SASRecBlock(d, heads, dropout) for _ in range(layers)])
        self.norm     = nn.LayerNorm(d)
        self.d = d; self.max_len = max_len

    def encode(self, seq):
        """seq: (B, L) item ids (0=pad)."""
        B, L = seq.shape
        positions = torch.arange(L, device=seq.device).unsqueeze(0)
        x = self.emb_drop(self.item_emb(seq) + self.pos_emb(positions))

        # causal mask
        causal = torch.triu(torch.ones(L, L, device=seq.device), diagonal=1).bool()
        pad_mask = (seq == 0)   # True where padded

        for blk in self.blocks:
            x = blk(x, attn_mask=causal, key_padding_mask=pad_mask)
        return self.norm(x)          # (B, L, d)

    def forward(self, seq, pos_items, neg_items):
        """
        seq:       (B, L)
        pos_items: (B,)
        neg_items: (B, num_neg)
        """
        h = self.encode(seq)[:, -1, :]          # last position (B, d)
        pos_e = self.item_emb(pos_items)         # (B, d)
        neg_e = self.item_emb(neg_items)         # (B, num_neg, d)

        pos_score = (h * pos_e).sum(-1)          # (B,)
        neg_score = (h.unsqueeze(1) * neg_e).sum(-1)  # (B, num_neg)
        return pos_score, neg_score

    def predict_all(self, seq):
        """Returns scores for all items given a sequence."""
        h = self.encode(seq)[:, -1, :]           # (B, d)
        all_e = self.item_emb.weight              # (n_items+1, d)
        return h @ all_e.T                        # (B, n_items+1)


