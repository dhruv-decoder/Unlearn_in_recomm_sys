#  5.  GRADIENT ASCENT UNLEARNING
# ═══════════════════════════════════════════════════════════

def gradient_ascent_unlearn(model, forget_loader, retain_loader,
                            epochs=EPOCHS_GA, ga_lr=5e-5, kl_weight=0.1):
    """
    Gradient Ascent (GA) + KL regularisation on retain set (GradDiff variant).

    - On forget set: *ascend* the BPR loss (push model away from those interactions)
    - On retain set: KL-diverge from original predictions to preserve utility

    This implements the approach from:
      "Machine Unlearning" (Bourtoule et al., 2021) +
      gradient difference regularisation (Yao et al., 2023)
    """
    # Save a frozen reference copy for KL computation
    ref_model = type(model)(model.item_emb.weight.shape[0] - 1).to(DEVICE)
    ref_model.load_state_dict(model.state_dict())
    ref_model.eval()
    for p in ref_model.parameters():
        p.requires_grad_(False)

    optimizer = torch.optim.Adam(model.parameters(), lr=ga_lr)

    retain_iter = iter(retain_loader)
    forget_losses, retain_kl_losses = [], []

    for epoch in range(epochs):
        model.train()
        epoch_forget_loss = 0
        epoch_retain_loss = 0

        for u, seq, pos, neg in tqdm(forget_loader,
                                     desc=f"  GA Unlearn epoch {epoch+1}/{epochs}",
                                     leave=False):
            seq, pos, neg = seq.to(DEVICE), pos.to(DEVICE), neg.to(DEVICE)

            # ── Gradient ASCENT on forget set ──
            pos_s, neg_s = model(seq, pos, neg)
            forget_loss  = -bpr_loss(pos_s, neg_s)   # note: negated → ascent

            # ── KL on retain set ──
            try:
                _, r_seq, r_pos, r_neg = next(retain_iter)
            except StopIteration:
                retain_iter = iter(retain_loader)
                _, r_seq, r_pos, r_neg = next(retain_iter)
            r_seq = r_seq.to(DEVICE)

            with torch.no_grad():
                ref_scores = ref_model.predict_all(r_seq)[:, 1:]   # exclude pad
                ref_probs  = F.softmax(ref_scores / 0.5, dim=-1)

            cur_scores = model.predict_all(r_seq)[:, 1:]
            cur_log_probs = F.log_softmax(cur_scores / 0.5, dim=-1)
            kl_loss = F.kl_div(cur_log_probs, ref_probs, reduction="batchmean")

            total_loss = forget_loss + kl_weight * kl_loss
            optimizer.zero_grad(); total_loss.backward(); optimizer.step()

            epoch_forget_loss += forget_loss.item()
            epoch_retain_loss += kl_loss.item()

        avg_f = epoch_forget_loss / len(forget_loader)
        avg_r = epoch_retain_loss / len(forget_loader)
        forget_losses.append(avg_f)
        retain_kl_losses.append(avg_r)
        print(f"    GA epoch {epoch+1}: ascent_loss={avg_f:.4f}  kl_loss={avg_r:.4f}")

    return forget_losses, retain_kl_losses


